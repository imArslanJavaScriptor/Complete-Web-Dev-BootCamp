// Task:
// Perform the following mathematical operations
// on the provided variables a and b

let a = 10
let b = 40

// 1- Add
function add() {
    return a + b
}

// Subtract
function sub() {
    return b - a
}

// Multiply
function mul() {
    return a * b
}

// Divide
function divide() {
    return  b / a
}

// Increment
function increment() {
    return ++a
}

// Decrement
function decrement() {
    return --a
}

// Reminder
function reminder() {
    return a % b
}


console.log(add())
console.log(sub())
console.log(mul())
console.log(divide())
console.log(increment())
console.log(decrement())
console.log(reminder())